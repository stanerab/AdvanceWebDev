            <h4><strong>About this book</strong></h4>
            The British Empire is often misunderstood. Judgments of it differ widely, from broadly adulatory - a 'great'
            enterprise, spreading 'civilization' through the world; to the blame that is often put on it for most of the
            world's ills today, including racism, exploitation and the problems of the Middle East.
            <br><br>
            In this provocative book, Bernard Porter argues that many of these judgments arise from some fundamental
            misreadings of the nature, causes and effects of British imperialism, which was a more complex, ambivalent
            and in some ways accidental phenomenon than it is often taken to be. Drawing on his fifty years' experience
            of research and writing on the subject, Porter aims to clear away many of the misconceptions that surround
            the story of the British Empire's rise, governance and fall; and to point some ways to a fairer (though not
            necessarily more favourable) assessment of it.
            <br><br>
            He addresses the connections of imperialism with capitalism, racism and British domestic culture, and ends
            with some reflections on the modern repercussions of both the Empire itself, and the myths which have sprung
            up around it.
