            <h4><strong>About this book</strong></h4>
            Combining insights from imperial studies and transnational book history, this provocative collection opens
            new vistas on both fields through ten accessible essays, each devoted to a single book. Contributors revisit
            well-known works associated with the British empire, including Charlotte Brontë's Jane Eyre, Thomas
            Macaulay's History of England, Charles Pearson's National Life and Character, and Robert Baden-Powell's
            Scouting for Boys.
            <br><br>
            They explore anticolonial texts in which authors such as C. L. R. James and Mohandas K. Gandhi chipped away
            at the foundations of imperial authority, and they introduce books that may be less familiar to students of
            empire. Taken together, the essays reveal the dynamics of what the editors call an "imperial commons," a
            lively, empire-wide print culture. They show that neither empire nor book were stable, self-evident
            constructs. Each helped to legitimize the other.
            <br><br>

            Contributors. Tony Ballantyne, Elleke Boehmer, Catherine Hall, Isabel Hofmeyr, Aaron Kamugisha, Marilyn
            Lake, Charlotte Macdonald, Derek Peterson, Mrinalini Sinha, Tridip Suhrud, André du Toit
