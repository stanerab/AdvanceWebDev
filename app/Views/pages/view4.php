            <h4><strong>About this book</strong></h4>
            There are so many School Histories of England already in existence, that it may perhaps seem presumptuous on
            the part of the authors of this' series to add six volumes more to the number. But they have their defence:
            the Oxford Manuals of English History are intended to serve a particular purpose. There are several good
            general histories already in use, and there are a considerable number of scattered 'epochs' or 'periods'.
            But there seems still to be room for a set of books which shall combine the virtues of both these classes.
            Schools often wish to take up only a certain portion of the history of England, and find one of the large
            general histories too bulky for their use. On the other hand, if they employ one of the isolated 'epochs' to
            which allusion has been made, they find in most cases that there is no succeeding work on the same scale and
            lines from which the scholar can continue his study and pass on to the next period, without a break in the
            continuity of his knowledge.
            <br><br>
            The object of the present series is to provide a set of historical manuals of a' convenient size, and at a
            very moderate price. Each part is complete in itself, but as the volumes will be carefully fitted on to each
            other, so that the whole form together a single continuous history of England, it will be possible to use
            any two or more of them in successive terms or years at the option of the instructor. They are kept
            carefully to the same scale, and the editor has done his best to put before the various authors the
            necessity of a uniform method of treatment.
